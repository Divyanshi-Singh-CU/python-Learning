#string to integer
num = "25"

print("Before:", num, type(num))

num = int(num)

print("After:", num, type(num))

#integer to float

num = 10

print("Before:", num, type(num))

num = float(num)

print("After:", num, type(num))


#float to int

num = 12.75

print("Before:", num, type(num))

num = int(num)

print("After:", num, type(num))

